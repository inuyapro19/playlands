        <h2>Entretenimiento</h2>
            <p>Para  Jean Piaget  el juego y los juguetes son considerados como "materiales útiles" para el desarrollo del pensamiento lógico, del lenguaje,  psicomotor, social  y cognitivo  del el niño. En Ayekantun  sabemos que los niños necesitan ser activos, estar en movimiento, dejar volar su imaginación  y sentirse libres, es por ello que consideramos al juego como un elemento básico en edades tempranas, que además de ser divertido,  favorece el desarrollo integral los niños y niñas.</p>

			<p>Por esta razón creamos diversos espacios, donde los niños y niñas puedan desenvolverse  libremente, sentirse seguros y acogidos,  interactuar con sus pares, escoger juegos según sus intereses  y desarrollar más su creatividad e imaginación.</p>

