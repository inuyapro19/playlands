        <h2>Lógica</h2>
            <p>El acto de pensar es aquel que pone en funcionamiento el cerebro humano para permitirle conocer, imaginar, abstraer, analizar o comparar el mundo que lo rodea o concebir fantasías. Para el pensamiento son indispensables los procesos cognitivos, pues a través de ellos somos consientes de nuestra condición físico-biológica y nos permiten percatarnos  de lo que sucede en nuestro alrededor.  La memoria, percepción y atención son procesos fundamentales para el aprendizaje  y por tanto deben ser estimulados desde edades tempranas.</p>

			<p>En Ayekantun consideramos que la estimulación  cognitiva promueve y favorece el desarrollo de  habilidades, competencias y capacidades en los niños y niñas, la capacidad de captar de los sentidos y estimulación sensorial, a demás de estimular el pensamiento y potenciar el desarrollo de este.</p>

			<p>Los talleres “Mente en acción y pensamiento en construcción”  fueron diseñados con el fin de potenciar  o mejorar  la capacidad mental de los niños(as) a través de actividades lúdicas como juegos de lógica y construcciones guiadas , los cuales  favorecen  el aprendizaje formal y de la vida cotidiana y social, a demás de potenciar su imaginación y creatividad.</p>

			<p>Dentro de las actividades que se realizaran en nuestros talleres podemos encontrar:</p>

			<h3>Pensamiento en construcción: </h3>
			<ul>
				<li>Actividades para estimular el proceso de atención selectiva y sostenida a nivel grafico (completar secuencias, reconocer objeto igual al modelo, pintar por un criterio, seguir series según criterio, Mándalas, identificar absurdos).</li>
				<li>Actividades para estimular el proceso de atención selectiva y sostenida a nivel simbólico (juegos dirigidos).</li>
				<li>Juegos con bloques lógicos de Dienes. </li>
				<li>Actividades para estimular el proceso de memoria a nivel grafico (ordenar secuencias , retención de imágenes.</li>
				<li>Actividades para estimular el proceso de memoria a nivel simbólico (juegos guiados).</li>
				<li>Actividades para estimular el proceso de percepción a nivel grafico (observar, seleccionar y organizar datos).</li>
				<li>Actividades para estimular el proceso de percepción a nivel simbólico  (asociaciones e interpretaciones).</li>
			</ul>

			<h3>Mente en acción:</h3>
			<ul>
				<li>Juegos de razonamiento lógico (juegos de seriación, conservación y clasificación).</li>
				<li>Juego simbólico (imitación de personas, animales y hechos). </li>
				<li>Juegos lógico-matemáticos (aprendiendo a contar, sumar y restar).</li>
				<li>Juegos lingüísticos - verbal (crear y contar cuentos, trabalenguas e historias). </li>
				<li>Juegos de construcción (rompecabezas, bloques, legos, jenga , figuras geométricas).</li>
				<li>Juegos de razonamiento (domino, lota, memorice).</li>
			</ul>
